#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*r;

	i = 0;
	r = 0;
	while (s[i])
	{
		if (s[i] == c % 128)
			r = (char *)&s[i];
		i++;
	}
	if (c % 128 == 0)
		return ((char *)&s[i]);
	return (r);
}
