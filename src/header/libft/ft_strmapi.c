#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*r;
	int		i;

	if (!s)
		return (0);
	i = 0;
	r = (char *)malloc(ft_strlen(s) * sizeof(char) + 1);
	if (!r)
		return (0);
	while (s[i])
	{
		r[i] = f(i, s[i]);
		i++;
	}
	r[i] = '\0';
	return (r);
}
