#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	r;

	if (!dst && size == 0)
		return (0);
	i = ft_strlen(dst);
	r = ft_strlen(src);
	j = 0;
	if (size <= i)
		r += size;
	else
		r += i;
	while (src[j] && i + 1 < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (r);
}
